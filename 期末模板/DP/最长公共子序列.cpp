#include <bits/stdc++.h>
using namespace std;
const int maxn = 1007;
int dp[maxn][maxn];
string s1, s2;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cin >> s1 >> s2;
    int n = s1.length(), m = s2.length();
    for (int i = 1; i <= n; i++){
        for (int j = 1; j <= m; j++){
            if (s1[i - 1] == s2[j - 1]){
                dp[i][j] = dp[i - 1][j - 1] + 1;
            }
            else {
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
            }
        }
    }
    cout << dp[n][m] << endl;
    return 0;
}


